function myFunction(){

	var x = document.getElementById("createpin");
	var y = document.getElementById("changetext");

	if(x.style.display === 'block' && y.innerHTML === '+'){
		x.style.display = 'none';
		y.innerHTML === 'x';

	}else{
		x.style.display = 'block';
		y.innerHTML === '+';
	}

}

